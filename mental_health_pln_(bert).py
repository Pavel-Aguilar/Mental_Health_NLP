"""# IMPORTACIONES Y CARGA DE DATOS"""

import pandas as pd
import numpy as np
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import LabelEncoder
from datasets import Dataset, DatasetDict
from sklearn.metrics import classification_report, confusion_matrix
import seaborn as sns
import matplotlib.pyplot as plt
from transformers import AutoModelForSequenceClassification, TrainingArguments, Trainer
import evaluate
import os
#CSV ORIGINAL
df = pd.read_csv("/content/drive/MyDrive/PDS/mental_health_pln/mental_health.csv")

#Se elimina la primera columna porque no aporta nada
df.drop(columns=['Unnamed: 0'], inplace=True)
df.rename(columns={'statement': 'text', 'status': 'label'}, inplace=True)
df = df.dropna(subset=['text'])

#se convierten etiquetas a numeros
le = LabelEncoder()
df['label'] = le.fit_transform(df['label'])

# Guardamos el mapa para saber qué es qué después
label_map = {index: label for index, label in enumerate(le.classes_)}
print(f"Clases detectadas: {label_map}")

#Divisón de datos
train_df, test_df = train_test_split(df, test_size=0.2, stratify=df['label'], random_state=42)

# 5. Convertir a formato Hugging Face Dataset
# Esto hace que el manejo de datos sea mucho más eficiente para la GPU
dataset = DatasetDict({"train": Dataset.from_pandas(train_df),"test": Dataset.from_pandas(test_df)})

print("\nDatos listos para BERT.")
print(dataset)

"""# Tokenización"""

from transformers import AutoTokenizer

# Usamos 'bert-base-cased' (Cased es mejor si las mayúsculas importan, o 'uncased' si no)
# Para salud mental, a veces MAYÚSCULAS significan gritos/estrés, así que probemos 'cased'.
model_checkpoint = "bert-base-cased"
tokenizer = AutoTokenizer.from_pretrained(model_checkpoint)

def preprocess_function(examples):
    # Truncation=True corta frases muy largas (límite de BERT es 512 tokens)
    return tokenizer(examples["text"], truncation=True, padding="max_length", max_length=128)

print("Tokenizando datos (esto es rápido)...")
tokenized_datasets = dataset.map(preprocess_function, batched=True)
print("Tokenización completada.")

"""# Configuración del modelo y entrenamiento"""

os.environ["WANDB_DISABLED"] = "true"
model_checkpoint = "bert-base-cased"
# TU RUTA ORIGINAL
model_save_path = "/content/drive/MyDrive/MAESTRIA/PLN/bert_final_model"

# --- 2. Lógica de Decisión: ¿Cargar o Entrenar? ---
if os.path.exists(model_save_path):
    print(f"✅ Modelo encontrado en: {model_save_path}")


    # Cargar desde TU carpeta
    model = AutoModelForSequenceClassification.from_pretrained(model_save_path)
    se_requiere_entrenamiento = False

else:
    print(f"❌ No se encontró modelo en: {model_save_path}")
    model = AutoModelForSequenceClassification.from_pretrained(model_checkpoint, num_labels=7)
    se_requiere_entrenamiento = True

metric = evaluate.load("accuracy")

def compute_metrics(eval_pred):
    logits, labels = eval_pred
    predictions = np.argmax(logits, axis=-1)
    return metric.compute(predictions=predictions, references=labels)

training_args = TrainingArguments(
    output_dir="./bert_temp_checkpoints",
    eval_strategy="epoch",
    save_strategy="epoch",
    learning_rate=2e-5,
    per_device_train_batch_size=16,
    per_device_eval_batch_size=16,
    num_train_epochs=3,
    weight_decay=0.01,
    load_best_model_at_end=True,
    seed=11,
    data_seed=11)

trainer = Trainer(
    model=model,
    args=training_args,
    train_dataset=tokenized_datasets["train"],
    eval_dataset=tokenized_datasets["test"],
    tokenizer=tokenizer,
    compute_metrics=compute_metrics)

# --- 4. Ejecución del Entrenamiento (Solo si es necesario) ---
if se_requiere_entrenamiento:
    print("\n🚀 Comienzo del entrenamiento...")
    trainer.train()

    print(f"\n💾 Guardando modelo final y tokenizer en: {model_save_path}")
    trainer.save_model(model_save_path)
    tokenizer.save_pretrained(model_save_path) # Importante guardar el tokenizer también
else:
    print("\n⏭️ Entrenamiento omitido. Pasando directo a evaluación.")

"""# Evaluación final"""

header("Evaluación del modelo final")

predictions = trainer.predict(tokenized_datasets["test"])
preds = np.argmax(predictions.predictions, axis=-1)
true_labels = predictions.label_ids

#Classification report
print("\nReporte de clasificación con BERT")
# Usamos le.classes_ para poner los nombres reales (Anxiety, Depression, etc.)
print(classification_report(true_labels, preds, target_names=le.classes_))

#Matríz de confusión
cm = confusion_matrix(true_labels, preds)
plt.figure(figsize=(10, 8))
sns.heatmap(cm, annot=True, fmt='d', cmap='Blues', xticklabels=le.classes_, yticklabels=le.classes_)
plt.ylabel('Verdadero')
plt.xlabel('Predicho')
plt.title('Matriz de Confusión - BERT')
plt.show()

#Se guarda el modelo final
model_save_path = "/content/drive/MyDrive/MAESTRIA/PLN/bert_final_model"
trainer.save_model(model_save_path)
print(f"Modelo guardado en: {model_save_path}")