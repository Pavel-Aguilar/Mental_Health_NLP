def vectorizar_lemas(lemas_limpios, modelo):
    vectores_palabras = [modelo[palabra] for palabra in lemas_limpios
                         if palabra in modelo.key_to_index]
    if len(vectores_palabras) > 0:
        return np.mean(vectores_palabras, axis=0)
    else:
        return np.zeros(VECTOR_SIZE)

