def limpiar_y_lematizar(texto):
    doc = nlp(texto.lower())
    lemmas = [token.lemma_ for token in doc
              if not token.is_stop and
                 token.lemma_ not in stop_words_en and
                 token.is_alpha and
                 len(token.lemma_) > 1]
    return lemmas

